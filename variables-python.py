#three types of variables - string, integer, float

#string - grouping of characters. can be numbers, letters, or symbols. quotes are placed around it.
greeting = "Hey there, "
print(greeting + "you brilliant person!")

#integer - whole numbers. don't need quotes around it
high_temperature = 10
low_temperature = -6
print(high_temperature + low_temperature)

#float - numbers that have a decimal
cat_food_price = 54.99
tax = .05
total = cat_food_price * tax + cat_food_price
print(total)